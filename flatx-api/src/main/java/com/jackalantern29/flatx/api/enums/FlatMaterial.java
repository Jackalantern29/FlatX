    final String legacy;
    final int id;
    final byte data;

    FlatMaterial() {
        this(null, -1, (byte)-1);
    }

    FlatMaterial(int id) {
        this(null, id);
    }

    FlatMaterial(int id, byte data) {
        this(null, id, data);
    }

    FlatMaterial(String legacy, int id) {
        this(legacy, id, (byte)0);
    }

    FlatMaterial(String legacy, int id, byte data) {
        if(id != -1 && legacy == null)
            this.legacy = name();
        else
            this.legacy = legacy;
        this.id = id;
        this.data = data;
    }

    public String getLegacyName() {
        return legacy;
    }

    public int getLegacyId() {
        return id;
    }

    public byte getLegacyData() {
        return data;
    }

    public boolean isLegacyAvailable() {
        return id != -1;
    }

    public static FlatMaterial fromLegacy(String legacy) {
        for(FlatMaterial flat : values()) {
            if(flat.getLegacyName().equals(legacy.toUpperCase()))
                return flat;
        }
        return null;
    }

    public static FlatMaterial getMaterial(String material) {
        return getMaterial(material, false);
    }

    public static FlatMaterial getMaterial(String material, boolean legacy) {
        for(FlatMaterial flat : values()) {
            if(legacy) {
                if (flat.getLegacyName().equals(material.toUpperCase()))
                    return flat;
            } else
                return FlatMaterial.valueOf(material.toUpperCase());
        }
        return null;
    }

    public static FlatMaterial getMaterial(int id) {
        return getMaterial(id, (byte)0);
    }

    public static FlatMaterial getMaterial(int id, byte data) {
        if(id != -1) {
            for(FlatMaterial flat : values()) {
                if(flat.getLegacyId() == id && flat.getLegacyData() == data)
                    return flat;
            }
        }
        return null;
    }
